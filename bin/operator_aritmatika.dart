void main() {
  var value1 = 10;
  var value2 = 2;

  print(value1 + value2); // Penjumlahan
  print(value1 - value2); // Pengurangan
  print(value1 * value2); // Perkalian
  print(value1 / value2); // Pembagian
  print(value1 ~/ value2); // Pembagian dengan pembulatan ke bawah
  print(value1 % value2); // Modulus
}